void storePath(uint8_t path_planned[], int parent[], int j, uint8_t *idx) {
    if (parent[j] == -1) {
        path_planned[(*idx)++] = j;
        return;
    }

    storePath(path_planned, parent, parent[j], idx);
    path_planned[(*idx)++] = j;
}


int minDistance(int dist[], int sptSet[]) {
    int min = INT_MAX, min_index;

    for (int v = 0; v < V; v++) {
        if (!sptSet[v] && dist[v] < min) {
            min = dist[v];
            min_index = v;
        }
    }

    return min_index;
}

void dijkstra(int graph[V][V], int src, int dest, uint8_t path_planned[], uint8_t *idx) {
    int dist[V];
    int parent[V];
    int sptSet[V];

    for (int i = 0; i < V; i++) {
        dist[i] = INT_MAX;
        parent[i] = -1;
        sptSet[i] = 0;
    }

    dist[src] = 0;

    for (int count = 0; count < V - 1; count++) {
        int u = minDistance(dist, sptSet);
        sptSet[u] = 1;

        for (int v = 0; v < V; v++) {
            if (!sptSet[v] && graph[u][v] && dist[u] != INT_MAX && dist[u] + graph[u][v] < dist[v]) {
                dist[v] = dist[u] + graph[u][v];
                parent[v] = u;
            }
        }
    }

    *idx = 0;
    storePath(path_planned, parent, dest, idx);
}


